
public class Operadores {
	public static void main(String[] args) {
		/*
		 * Lista de operadores com exemplo de uso.
		 */
		
		//Sufixais: var++, var--
		  int var = 0;
		  System.out.println(var++);
		
		//Prefixais: --num, ++num
		  int num1 = 10;
		  System.out.println(--num1);
		  
		//Aritméticos: *, +, -, /, %
		  int num2 = 25;
		  System.out.println(num2 - 5);
		  
		//Concatenação: +
		  double num3 = 10.5;
		  System.out.println("Valor é: "+num3);
		  //Outro Exemplo
		  String a = "A", b = "B";
		  System.out.println(a + b);
		  
		//Igualdade: ==, !=
		  int num4 = 10, num5 = 20;
		  if(num4 == num2);
		  System.out.println("Iguais");
		  if(num4 != num5);
		  System.out.println("Diferentes");
		
		//Lógicos: &&, ||
		  int num6 = 10, num7= 11;
		  if(num6 == 10 && num7 != 10)
			  System.out.println("Verdadeiro");
		  else
			  System.out.println("Falso");
		  
		//Atribuição: =, +=, -=, *=, /=, %=
		  int num8 = 30;
		  System.out.println(num8 += 20);
		  System.out.println(num8 /= 2);
		
		//Comparativos: >=, <=, >, <
		
	}
	
}
