public class Programa {
	public static void main (String args[]) {
		/*
		 * Mostrar a média de duas notas.
		 * */
		
		double notaUm = 10.0;
		double notaDois = 7.0;
		double media = (notaUm + notaDois) / 2;
		System.out.print("A media é: "+ media);
		System.out.printf("\nA media é: %.2f ", media);
	}
}
